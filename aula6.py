# conversão de tipos, coerção
# type convertion, typecasting, coercion
# é o ato de converter um tipo em outro
# tipo imutáveis e primitivos:
# str, int, float, bool

print(1 + 1)
print('a' + 'b')
# print('1' + 1) TypeError

print(int(1.0))
print(float(1))
print(str(1), type(str(1)), type(1))
print(int('1'), type(int('1')), type('1'))
print(float('1') + 1)
print(bool(1), bool(0), bool(2), bool(), bool('T'), bool(''))
print(str(11) + 'b')
