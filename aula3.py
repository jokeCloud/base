"""
Python = linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
"""
print(123)

# Aspas simples ''
print('Michael Alzawsky')

# Aspas duplas ""
print("Sully Sullivan")

# Não utilizar escape ou r, fica muito confuso
# Escape \
print("Sully \"Sullivan\"")

# r regex
print(r"Sully \"Sullivan\"")

# melhor fazer assim
print('Sully "Sullivan"')
